// Utility: pick N unique random items from an array
export function pickRandom<T>(arr: T[], count: number): T[] {
  const shuffled = [...arr].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, Math.min(count, arr.length));
}

// Utility: pick 1 random item
export function pickOne<T>(arr: T[]): T {
  return arr[Math.floor(Math.random() * arr.length)];
}

// Dataset selections per generator type
export function getDatasetSelections(generatorType: string): Record<string, string> {
  switch (generatorType) {
    case "character":
      return {
        dataName: pickOne(characterNames),
        dataTrait: pickOne(personalityTraits),
        dataPower: pickOne(magicAbilities),
        dataRealm: pickOne(cultivationRealms),
        dataConflict: pickOne(storyConflicts),
      };
    case "world":
      return {
        dataKingdom: pickOne(kingdomNames),
        dataMonster: pickOne(monsterSpecies),
        dataMagic: pickOne(magicAbilities),
        dataConflict: pickOne(storyConflicts),
      };
    case "cultivation":
      return {
        dataRealms: pickRandom(cultivationRealms, 5).join(", "),
      };
    case "villain":
      return {
        dataName: pickOne(characterNames),
        dataTrait: pickOne(personalityTraits),
        dataPower: pickOne(magicAbilities),
        dataSect: pickOne(sectNames),
      };
    case "name":
      return {
        dataSamples: pickRandom(characterNames, 3).join(", "),
      };
    case "plot":
      return {
        dataConflict: pickOne(storyConflicts),
        dataKingdom: pickOne(kingdomNames),
        dataMonster: pickOne(monsterSpecies),
      };
    default:
      return {};
  }
}
